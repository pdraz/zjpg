// 32x32 RGB test image: 3072 bytes (32*32*3)
// Creates a gradient pattern that better showcases chroma subsampling behavior
// Diagonal gradients with smooth color transitions simulate natural image characteristics
pub const TEST_IMAGE_32x32_RGB = blk: {
    @setEvalBranchQuota(2000);
    var img: [3072]u8 = undefined;
    for (0..32) |y| {
        for (0..32) |x| {
            const idx = (y * 32 + x) * 3;

            // Create diagonal gradients for each channel
            // This provides smooth transitions that compress well with chroma subsampling
            const x_norm = @as(f32, @floatFromInt(x)) / 31.0;
            const y_norm = @as(f32, @floatFromInt(y)) / 31.0;

            // Red: left-to-right gradient
            const r = 64.0 + 191.0 * x_norm;

            // Green: top-to-bottom gradient
            const g = 64.0 + 191.0 * y_norm;

            // Blue: diagonal gradient
            const b = 64.0 + 191.0 * ((x_norm + y_norm) / 2.0);

            img[idx] = @intFromFloat(@max(0.0, @min(255.0, r)));
            img[idx + 1] = @intFromFloat(@max(0.0, @min(255.0, g)));
            img[idx + 2] = @intFromFloat(@max(0.0, @min(255.0, b)));
        }
    }
    break :blk img;
};
